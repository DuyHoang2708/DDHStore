-- SQL Script for Creating Orders and Order Detail Tables in Oracle

-- 1. Create ORDERS Table
CREATE TABLE ORDERS (
    ORDER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER NOT NULL,
    ORDER_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    TOTAL_AMOUNT NUMBER(18, 2) NOT NULL,
    STATUS VARCHAR2(50) DEFAULT 'PENDING' NOT NULL,
    ADDRESS_ID NUMBER,
    CONSTRAINT FK_ORDER_USER FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID),
    CONSTRAINT FK_ORDER_ADDRESS FOREIGN KEY (ADDRESS_ID) REFERENCES ADDRESSES(ADDRESS_ID)
);

-- 2. Create ORDER_DETAIL Table
CREATE TABLE ORDER_DETAIL (
    ORDER_DETAIL_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ORDER_ID NUMBER NOT NULL,
    PRODUCT_ID NUMBER NOT NULL,
    QUANTITY NUMBER NOT NULL,
    PRICE NUMBER(18, 2) NOT NULL,
    CONSTRAINT FK_DETAIL_ORDER FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ORDER_ID) ON DELETE CASCADE,
    CONSTRAINT FK_DETAIL_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT(PRODUCT_ID)
);

-- Comments for documentation
COMMENT ON COLUMN ORDERS.STATUS IS 'Can be PENDING, PROCESSING, SHIPPED, COMPLETED, CANCELLED';
